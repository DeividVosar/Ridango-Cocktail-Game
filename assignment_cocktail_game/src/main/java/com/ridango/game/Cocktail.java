package com.ridango.game;

public class Cocktail {
    private String strDrink;
    private String strInstructions;
    private String strCategory;  // Field for category
    private String strGlass;  // Field for glass type
    private String strIngredients;  // Field for ingredients (concatenated string)

    // Getters and Setters
    public String getStrDrink() {
        return strDrink;
    }

    public void setStrDrink(String strDrink) {
        this.strDrink = strDrink;
    }

    public String getStrInstructions() {
        return strInstructions;
    }

    public void setStrInstructions(String strInstructions) {
        this.strInstructions = strInstructions;
    }

    public String getStrCategory() {
        return strCategory;
    }

    public void setStrCategory(String strCategory) {
        this.strCategory = strCategory;
    }

    public String getStrGlass() {
        return strGlass;
    }

    public void setStrGlass(String strGlass) {
        this.strGlass = strGlass;
    }

    public String getStrIngredients() {
        return strIngredients;
    }

    public void setStrIngredients(String strIngredients) {
        this.strIngredients = strIngredients;
    }
}
